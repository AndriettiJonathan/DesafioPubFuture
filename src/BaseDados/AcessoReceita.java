package BaseDados;

/**
 *
 * @author Jonathan Andrietti
 */
public class AcessoReceita {
    
    private int Codigo;
    private String Nome;
    private int Valor;
    private String Treceita;
    private int Dpago;
    private int Dpagar;
    private String Conta;
    private String Busca;
    
    

    /**
     * @return the codigo
     */
    public int getCodigo() {
        return Codigo;
    }

    /**
     * @param Codigo the codigo to set
     */
    public void setCodigo(int Codigo) {
        this.Codigo = Codigo;
    }

    /**
     * @return the Nome
     */
    public String getNome() {
        return Nome;
    }

    /**
     * @param Nome the Nome to set
     */
    public void setNome(String Nome) {
        this.Nome = Nome;
    }

    /**
     * @return the Valor
     */
    public int getValor() {
        return Valor;
    }

    /**
     * @param Valor the Valor to set
     */
    public void setValor(int Valor) {
        this.Valor = Valor;
    }

    /**
     * @return the Treceita
     */
    public String getTreceita() {
        return Treceita;
    }

    /**
     * @param Treceita the Treceita to set
     */
    public void setTreceita(String Treceita) {
        this.Treceita = Treceita;
    }

    /**
     * @return the Dpago
     */
    public int getDpago() {
        return Dpago;
    }

    /**
     * @param Dpago the Dpago to set
     */
    public void setDpago(int Dpago) {
        this.Dpago = Dpago;
    }

    /**
     * @return the Dpagar
     */
    public int getDpagar() {
        return Dpagar;
    }

    /**
     * @param Dpagar the Dpagar to set
     */
    public void setDpagar(int Dpagar) {
        this.Dpagar = Dpagar;
    }

    /**
     * @return the Conta
     */
    public String getConta() {
        return Conta;
    }

    /**
     * @param Conta the Conta to set
     */
    public void setConta(String Conta) {
        this.Conta = Conta;
    }

    /**
     * @return the Busca
     */
    public String getBusca() {
        return Busca;
    }

    /**
     * @param Busca the Busca to set
     */
    public void setBusca(String Busca) {
        this.Busca = Busca;
    }
    
}
