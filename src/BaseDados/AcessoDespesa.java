package BaseDados;

    

public class AcessoDespesa {
    
    private int Codigo;
    private int ValorD;
    private int DpagoD;
    private String TipoD;
    private int DpagarD;
    private String ContaD;
    private String Busca;

    /**
     * @return the ValorD
     */
    public int getValorD() {
        return ValorD;
    }

    /**
     * @param ValorD the ValorD to set
     */
    public void setValorD(int ValorD) {
        this.ValorD = ValorD;
    }

    /**
     * @return the DpagoD
     */
    public int getDpagoD() {
        return DpagoD;
    }

    /**
     * @param DpagoD the DpagoD to set
     */
    public void setDpagoD(int DpagoD) {
        this.DpagoD = DpagoD;
    }

    /**
     * @return the TipoD
     */
    public String getTipoD() {
        return TipoD;
    }

    /**
     * @param TipoD the TipoD to set
     */
    public void setTipoD(String TipoD) {
        this.TipoD = TipoD;
    }

    /**
     * @return the DpagarD
     */
    public int getDpagarD() {
        return DpagarD;
    }

    /**
     * @param DpagarD the DpagarD to set
     */
    public void setDpagarD(int DpagarD) {
        this.DpagarD = DpagarD;
    }

    /**
     * @return the ContaD
     */
    public String getContaD() {
        return ContaD;
    }

    /**
     * @param ContaD the ContaD to set
     */
    public void setContaD(String ContaD) {
        this.ContaD = ContaD;
    }

    /**
     * @return the Codigo
     */
    public int getCodigo() {
        return Codigo;
    }

    /**
     * @param Codigo the Codigo to set
     */
    public void setCodigo(int Codigo) {
        this.Codigo = Codigo;
    }

    /**
     * @return the Busca
     */
    public String getBusca() {
        return Busca;
    }

    /**
     * @param Busca the Busca to set
     */
    public void setBusca(String Busca) {
        this.Busca = Busca;
    }
    
}
